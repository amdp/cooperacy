  <p class="subheading freedom">Autumn 2015 – Cooperacy @ the IASC Conference The City as a commons in Bologna</p>
  <img src="{{asset('images/commonsbologna.png')}}" alt="null" />
  <p class="base"><br>
  Cooperacy paper is selected for the IASC Conference The City as a commons in Bologna and Alessandro is encharged to explain the model to the participants of the conference. During a speech by Cristian Iaione he suggests to have a common fund for changing the current economic system that is the principle obstacle against a culture based on the commons framework. The idea is appreciated with smiles and laughs because of its eccentric nature. When Alessandro goes back home he understands that a collective funding could be the missing piece of the Cooperacy project.</p>

