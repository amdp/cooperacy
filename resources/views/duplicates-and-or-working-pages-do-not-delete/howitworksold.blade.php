              <p style="color:#BB66DD; font-size: 24px; font-weight:700; margin-bottom:10px;">HOW IT WORKS</p>
              <p style="color:#BB66DD; font-size: 20px; font-weight:700; margin-bottom:10px;">JOINING EMPLOYMENT AND ENTERPRENEURSHIP</p>
              <p style="color:#BB66DD; font-size: 16px; font-weight:700; margin-bottom:20px">THE COOPERACY WHITE PAPER</p>
              <p style="color: #555555; font-size: 16px; text-align: justify;">Cooperacy is a research project which is based on special kind of collective fund developed to change the way current economy works. It has an internal pool of ideas that can be realized or supported. Its purpose is to generate a different social system economy based on equivalence and transparency. Everyone can participate with 1 euro per month.</p>
              <p><em>Note: in this white paper we use the &ldquo;singular they&rdquo;.</em></p>
              <p style="color:#BB66DD; font-size: 20px; font-weight:700; margin-bottom:10px;">DESIGN DEMOCRACY</p>
              <p style="color:#BB66DD; font-size: 16px; font-weight:700; margin-bottom:20px">VOTING IDEAS</p>
              <p style="color: #555555; font-size: 16px; text-align: justify;">People will, in the long run, decide the progress of the world voting local and worldwide ideas and not people. This will provide better allocation of hudgets, that is budget and human time and motivation, nowadays not efficiently managed.</p>
              <p style="color:#BB66DD; font-size: 16px; font-weight:700; margin-bottom:20px">NATURALLY ENJOYABLE LIVES</p>
              <p style="color: #555555; font-size: 16px; text-align: justify;">Every single individual will, in the short run, have the possibility to develop their life and activity they were made for, submitting their ideas and being financed by those who believe in them. The &ldquo;possibility&rdquo;, in the long run, will be more and more &lsquo;possible&lsquo; and widespread as the collective capital of Cooperacy will grow. Ideas will not make people rich, but give them equivalent funds or salaries to have the lives they would naturally enjoy the best.</p>
              <p style="color:#BB66DD; font-size: 16px; font-weight:700; margin-bottom:20px">TRANSFORM THE COLLECTIVE DECISION MAKING</p>
              <p style="color: #555555; font-size: 16px; text-align: justify;">The world will be a place for experimentation, as different kind of &ldquo;political&rdquo; governments that we call &ldquo;organizational modules&rdquo; could be activated through Cooperacy, in order to allow everyone to try a different way of living. This will make politics obsolete and direct participatory democracy common. The same lives will be decided according to lifestyles and not according to fame or money. It will be like a role-playing game in which everyone of us will choose their most preferred lifestyle, from a punk carpenter who studied chemical engineering who lives close to a tropical jungle to an analfabeth musician who crafts pottery and lives in a skyscraper.</p>
              <p style="color:#BB66DD; font-size: 16px; font-weight:700; margin-bottom:20px">COOPERACY IS BASED ON HUMANS AND COMPUTERS</p>
              <p style="color: #555555; font-size: 16px; text-align: justify;">Cooperacy will analyze anything developed within itself. Due to its complete transparency, participants are owners and while they decide together they would contribute to a database which will allow Cooperacy to machine-learn continuously about the experiences of the ideas of the humans. The system will be probably based on an underlying public-blockchain structure with 100% transparency for transactions safety.<br />
                What does it mean? In simple words, Cooperacy will be based on a system that will compare all the ideas and, through the data we provide in those ideas, learn and give advice in order to make the ideas happen or warn when the ideas could fail.<br />
                This could seem a big controlling machine system, but there&#8217;s a trick: every human is allowed to communicate to the system their happiness, and the system will base its choices also on the most human, irrational and personal information of all: mood.<br />
                The data will be trasparent for every participant, and owned by everyone.<br />
                Powered by big data and human moods, the system will create an equilibrium of efficiency and humanity, and will behave like a single big organism or ecosystem.<br />
                In simplest words, the computers act as a &ldquo;memory&rdquo; of the system that every human can access in order to make better decisions.<br />
                All the profit goes back into the bank and can be only used to fund other ideas.</p>
              <p style="color:#BB66DD; font-size: 20px; font-weight:700; margin-bottom:10px;">A REDISTRIBUTED COMMON POOL</p>
              <p style="color: #555555; font-size: 16px; text-align: justify;">Being a collective fund, any idea is owned by Cooperacy. So every surplus gets back to Cooperacy and makes the fund to become richer and support new ideas. Corruption tends to become automatically obsolete, as everything is completely transparent when an idea is approved, and it generates wealth for everyone anyways. Every project&#8217;s margins in fact go back to Cooperacy in order to gave birth to other projects.<br />
                As an example, let&#8217;s imagine we are 100k people and have a 300k fund. Many ideas arise, let&#8217;s imagine 50, and 30% of them get funds: 15. 5 of these don&#8217;t manage to do well, but the rest do. Maybe we invested 200k in the 10 ideas left. The ideas start to produce, and their margins, let&#8217;s imagine 40k a year, go bank to the fund. It&#8217;s just an example, but in five years the fund will be replenished and could fund other ideas.<br />
                What about the participants? The people who developed the ideas get a salary for doing their job obviously.</p>
              <p style="color:#BB66DD; font-size: 20px; font-weight:700; margin-bottom:10px;">BASED ON SCIENCE</p>
              <p style="color: #555555; font-size: 16px; text-align: justify;">Nobel Prize Elinor Ostrom observations, most recent collective intelligence insights and a science-based cooperative model is at the base of Cooperacy. The same machine learning information will be used at a scientific level to refine and upgrade the same Cooperacy system. The approach is multidisciplinary, based on diversity, integration-understanding, synergy, evolution and joins sociology, psychology, neuroscience, evolutionary biology, philosophy, economics, zoology, ethology, anthropology, semiotics, organization studies, system thinking and similar scientific spheres.</p>
              <p style="color:#BB66DD; font-size: 20px; font-weight:700; margin-bottom:10px;">A PROPORTIONAL DISTRIBUTION OF MONEY</p>
              <p style="color: #555555; font-size: 16px; text-align: justify;">If in Cooperacy the participants want some improvement or some change, they may issue an idea. This idea is like a referendum or a public vote for an institutional change, or a &ldquo;law&rdquo;. In Cooperacy there are no laws but agreements. There is no intention to find guilt or punishment, but to find solution to accomplish a desired situation or to avoid the problem to happen again. In this case too, ideas and context have a stronger value than obligations and punishments. It is more based on procedural justice and agreements than civil or common law, that is, codes of conduct or previous decisions by courts. Experimental setups may be triggered, in order to try different options and possibilities. The most important thing to understand is that the majority in a democratic consultation is approved, but the minority -or minorities- are declared experimental and they have the right to try a different solution, or get part of the budget to do it. Moreover, there are 7 ways to solve the situation where multiple ideas are desired by the participants (let&#8217;s take the idea of safety vs. innovation):</p>
              <p>Turns: two or more ideas are tested globally, and the effects are observed. The most preferred one is chosen. = One year a safe choice, the next an innovative one.<br />
                Observation: results of current solution are awaited and finally observed. The most preferred one is chosen. = Current safety brings no growth. We should innovate a bit.<br />
                Values: more general perspectives about life are taken into account in order to decide, like group benefit over individual one, safety over innovation or happiness over safety.<br />
                Truth: participants express in a very direct way their desires and knowledge: a hidden common truth is acknowledged and taken as a base. = We want innovation!<br />
                Space: sometimes separation brings good benefits, and instead of turns, areas work better. = Southern countries will innovate and northern ones remain in safety.<br />
                Combos: apparent conflicts may be part of a better and more efficient solution when joined together: a combination of the different solution is tried.<br />
                Roles: the different ideas are seen as part of a system. The industrialized areas experiment innovation while rural ones remain traditional and safe.</p>
              <p><p style="color:#BB66DD; font-size: 20px; font-weight:700; margin-bottom:10px;">COOPERACY IS AFTF</p>
                <p style="color: #555555; font-size: 16px; text-align: justify;">The voting process will be Anonymous First, Transparent Forever-after: the methodology we follow is similar to the Delphi method, and is also based on the Indaba or the Open Space Technology traditions that both came from the African social technologies. This process allows the maximization of the collective intelligence according to Page and Surowieki (&lsquo;Wisdom of the Crowds&rsquo; and &lsquo;Diversity Theorem&rsquo;), it respects the diversities while integrating them like the &lsquo;fork&rsquo;/&lsquo;join&rsquo; process in software development. In our voting process, keeping it anonymous at the beginning makes any influence to be reduced, while turning it transparent afterwards makes us grow and being mature in coping with truth. The information with this process is available to anybody anytime, but it doesn&#8217;t have an influential weight before the decision is made. This allows you to choose the project in which you want to work to, or propose yours, in full anonymity until the project reaches its minimum hudget (human resources budget + financial budget), after that, everything will always be public-blockchained-100% transparent. Anonymity allows to boost collective intelligence first, and transparency boosts cooperation afterward.</p>
                <p style="color:#BB66DD; font-size: 20px; font-weight:700; margin-bottom:10px;">GRANULAR, MODULAR AND A LITTLE BIT LIKE A FRACTAL</p>
                <p style="color: #555555; font-size: 16px; text-align: justify;">If we imagine many similar ideas, all over the world, like opening a bakery or developing a yoga course, we&#8217;ll see that many of them are similar and have common progresses. Every project has internal micromodules and microtasks (granularity) and all similar microtasks that resemble each other are connected together and integrated with the machine learning. All our activities will be like a colourful fractal in which any little part resembles the whole.</p>
