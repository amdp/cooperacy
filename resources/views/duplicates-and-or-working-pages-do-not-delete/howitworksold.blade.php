                <p style="color:#BB66DD; font-size: 20px; font-weight:700; margin-bottom:10px;">PERSONALITY BASED</p>
                <p style="color: #555555; font-size: 16px; text-align: justify;">According to latest research (Tyler, Why people cooperate; Mulder et al. 2006) intrinsic social motivations (working for a good cause or for what your personality really needs) are a stronger drive than extrinsic-instrumental motivations (get to the end of the month or make more money). This is why an Open Space labour market where everyone choses what they want to do according to their personality will be more productive that a standard one, and make people happier.</p>
                <p style="color:#BB66DD; font-size: 20px; font-weight:700; margin-bottom:10px;">FULL MOBILITY AND BANKRUPT PROOF</p>
                <p style="color: #555555; font-size: 16px; text-align: justify;">Thanks to the cooperative production system, who works in a field can choose to work in the same field in another project, like as a person working for a smartphone company would switch to a computer one, or a person working in a Cooperacy-funded bakery in Cape Town may transfer to Beijing and work in another Cooperacy-funded bakery. If one product, like some &ldquo;fancy pots&rdquo;, produced by one Cooperacy project, is not sold anymore because another Cooperacy idea, &ldquo;colourful fancy pots&rdquo;, has a better product, nobody is fired: the production of the old one, &ldquo;fancy pots&rdquo;, switches to the new, &ldquo;colourful fancy pots&rdquo;:, and your job is safe as the markets is demanding more and the company needs to grow!</p>
                <p style="color:#BB66DD; font-size: 20px; font-weight:700; margin-bottom:10px;">EQUISALARIES</p>
                <p style="color: #555555; font-size: 16px; text-align: justify;">The wages will use the superhit system, that averages the wages that have similar amounts in order to keep fairness, and specifies those that are way more higher -or lower- than the average. Unwanted jobs, like cleaning toilets, will raise up their wages until people will like to do them while wanted ones, like being a sport star, will never be ridiculous high again.</p>
                <p style="color:#BB66DD; font-size: 20px; font-weight:700; margin-bottom:10px;">ALMOST AT 0% RISK</p>
                <p style="color: #555555; font-size: 16px; text-align: justify;">The investment per person in Cooperacy is 1 euro or purchasing equivalent. This means, to give the idea, one half liter bottled orange juice. So, if in Madagascar an orange juice is 1000 ariary, the contribution will be 0.30 euros approximately.</p>
                <p style="color:#BB66DD; font-size: 24px; font-weight:700; margin-bottom:10px;">THE COOPERACY GOVERNANCE</p>
                <p style="color: #555555; font-size: 16px; text-align: justify;">The Cooperacy governance is modular. There are only two degrees, the &ldquo;role&rdquo; and the &ldquo;participant&rdquo; one. Participants may assume a role, and have the leadership over a subject. For example, if you have an idea to sell special flowers, you have the role to manage the idea. But if you just want to describe the idea and let anybody who wants to accomplish it, then your role of manager will be held by someone else. &ldquo;Roles&rdquo; have the right to issue direct decisions over modules. A dual leadership is advised, so we can imagine many modules intertwined in which two organizers take decisions in each module. Roles are one or maximum two, the suggested number being two. When the roles issue direct decisions, these can be adversed by participants. The tool is again Cooperacy. The governance is local, salaries are issued automatically, so Roles cannot impose on participants and participants don&#8217;t need to wait centralized justice to act and support them because they are part of the governance, in every project. The power of every role is functional, not executive. That&#8217;s why roles &#8220;function&#8221;, but do not dominate, &#8220;request&#8221; but do not command. </p>
