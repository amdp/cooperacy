  <br><br><p class="base">The Cooperacy governance is the way participants take decisions according to cooperation science. Every participant has the right to be part of the Cooperacy governance, that is based on few principles.</p><br /><br />

  <p class="subheading freedom center space">MODULES</p><br/>
  <p class="base">Cooperacy is made of modules. Every module is based on the most simple form of cooperation: two or more people doing something together. Every module should have two coordinators. The coordinators may be changed anytime upon the participants’ will or after the coordinators resignation. Every module may have submodules.<br />
  </p><br/>

  <p class="subheading freedom center space">DECISIONS</p><br/>
  <p class="base">Coordinators have the right to take direct decisions. Only when their decision is publicly communicated it becomes effective. When they are in contrast or unsure about what decision to take, coordinators can ask advice to the participants or start a voting phase.<br><br>
  Participants may oppose the decision. If it&#8217;s just one of them, the opposition is recorded or, when possible, creatively integrated. If the participants are at least two<SUP>2</SUP>, the coordinators should creatively integrate the opposition according to the seven <a href="http://cooperacy.org/solutions" class="care">solutions</a> or start a voting phase.<br><br>
  Participants may also propose an idea through voting or asking the coordinators for direct approval. The coordinators have no right to vote when they are the voting main subject.
  </p><br/>
  <p class="subheading freedom center space">VOTING</p><br/>
  <p class="base">Cooperacy is a design democracy, that is, the modules vote ideas first, people afterwards. There is no censorship about the ideas, but they must respect the participant’s enjoyment. The voting is never secret.<br>
  The decision making is based on approval voting, that is, everyone may vote with a single vote all the ideas they want. If there is not a clear orientation<SUP>2</SUP> after the voting phase, the participants leave their choice to the coordinators or choose with them amongst the seven possible <a href="http://cooperacy.org/solutions" class="care">solutions</a> for the realization of the module’s will:<br>
  - Turns or experimentations, starting from the most voted<br>
  - Give a definite time to test the previous situation and then vote again<br>
  - Change the context and pleasantness of the voting process and vote again<br>
  - Retrieve more information, listen to all the people positions and decide again<br>
  - Separate the module in new ones, splitting eventual budgets<br>
  - Evaluate things through common values or objective criterias and vote again<br>
  - Integrate creatively the different solutions or give them specific roles<br></p>
  <br><br>

  <p class="mini">1: DUAL COORDINATION The idea of a coordinating pair came up in order to avoid power to be concentrated in just one person, that receives huge responsibilities and is alone in facing them, or in more than two people, that may generate internal conflictual groups.<br>
  2: VOTING IS INFORMATIVE The voting results are indicators of the participants will, they do not explicitly determine a final decision, because creative proportionality and integration are at the base of cooperation, not winning and losing.</p>

