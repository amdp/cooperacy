  <br><br><p class="base">The Cooperacy governance is the way <a class="aue" href="/participate">participants</a> take decisions according to cooperation science. Cooperacy is based on this governance, but you can apply it to any group or organisation you may participate in. The Cooperacy governance is based on few principles.</p><br /><br />

  <p class="subheading freedom center space">MODULES</p><br/>
  <p class="base">The Cooperacy governance is made of modules, that can have submodules. Every module is based on the most simple form of <a class="aue" href="/cooperation">cooperation</a>: two or more people doing something together. Every module should have two coordinators, that may be changed anytime upon the participants’ will or after their resignation. Anybody can candidate for the coordination anytime.<br />
  </p><br/>

  <p class="subheading freedom center space">DECISIONS</p><br/>
  <p class="base">Coordinators have the right to take direct decisions. Only when their decision is publicly communicated it becomes effective. When they are in contrast or unsure about what decision to take, coordinators can ask advice to the participants or start a voting phase.<br>
  Participants can propose decisions as well through voting or asking the coordinators for direct approval. The coordinators have no right to vote when they are the voting main subject.<br>
  Participants may also oppose the coordinators decisions. If it's just one of them, the opposition is recorded or, when possible, proportionally and creatively <a class="auf" href="/integration">integrated</a>. If the opposing participants are at least two<SUP>1</SUP>, the coordinators should proportionally and creatively integrate the opposition according to the seven <a class="auf" href="/solutions">solutions</a> or alternatively start a voting phase.<br><br>

  </p><br/>
  <p class="subheading freedom center space">VOTING</p><br/>
  <p class="base">Cooperacy is a design democracy, that is, the modules vote ideas first, people afterwards. So decisions are voted independently from the people who will enact them, as they will be chosen <i>on a second step</i>. The voting is anonymous during its voting phase, but becomes transparent once the results are shown.<br>
  The decision making is based on approval voting, that is, everyone may vote with a single vote all the ideas or alternative decisions they want. If there is not a clear orientation<SUP>2</SUP> after the voting phase, the participants leave their choice to the coordinators. They may also choose with the coordinators which one of the seven possible <a class="auf" href="/solutions">solutions</a> to apply for the realization of the module’s will:<br>
  - Turns or experimentations, starting from the most voted<br>
  - Give a definite time to test the previous situation and then vote again<br>
  - Change the context and pleasantness of the context or the voting process and vote again<br>
  - Retrieve more information, listen to all the people positions and decide again<br>
  - Separate the module in new ones, splitting eventual budgets<br>
  - Evaluate things through data, common values or objective criterias and vote again<br>
  - Integrate creatively the different solutions or give them specific roles<br></p>
