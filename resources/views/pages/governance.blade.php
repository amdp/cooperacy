  <p class="subheading freedom center space">DECISIONS</p><br/>
  <p class="base">Coordinators may take direct decisions in their module. Only when their decision is publicly communicated it becomes effective. When they are in contrast or unsure about what decision to take, coordinators can ask advice to the participants or start a voting phase.<br><br>

  Participants can propose decisions as well through voting or asking the coordinators for direct approval. The coordinators should abstain when they are the voting main subject.<br>
  Participants may also oppose the coordinators decisions. If the opposing participants are at least two<SUP>1</SUP>, the coordinators should proportionally and creatively <a class="ad" href="/integration">integrate</a> the opposition according to the seven <a class="ae" href="/integration#solutions">solutions</a> or alternatively start a voting phase.<br><br>

  If just a single participants proposes or opposes, but nobody secondes, the single instance is recorded or, when possible, but not always, proportionally and creatively <a class="ad" href="/integration">integrated</a>.<br>

