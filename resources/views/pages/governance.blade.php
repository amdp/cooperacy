  <p class="subheading freedom center space">VOTING</p><br/>
  <p class="base">Cooperacy is a design democracy: we vote ideas first, people afterwards. Decisions are voted independently from the people who will enact them, as they will be chosen <i>on a second step</i>.<br><br>
  The voting can be anonymous during the voting phase, but becomes transparent once the results are shown. All voting results are transparent.<br><br>
  The decision making is based on approval voting, that is, everyone may vote with a single vote all the ideas or alternative decisions they want. If there is not a clear orientation<SUP>2</SUP> after the voting phase, the participants leave their choice to the coordinators. They may also choose with the coordinators which one of the seven possible <a class="af" href="/solutions">solutions</a> to apply for the realization of the module’s will:<br><br>
  - Turns or experimentations, starting from the most voted<br>
  - Give a definite time to test the previous situation and then vote again<br>
  - Change the context and pleasantness of the context or the voting process and vote again<br>
  - Retrieve more information, listen to all the people positions and decide again<br>
  - Separate the module in new ones, splitting eventual budgets<br>
  - Evaluate things through data, common values or objective criterias and vote again<br>
  - Combine using the <a class="af" href="/integration">proportional creative integration</a> the different solutions or give people -or the same solutions- specific roles<br></p>
