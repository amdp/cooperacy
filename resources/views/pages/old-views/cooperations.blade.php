<p style="color: #555555; font-size: 16px;">
              Cooperation means acting together, with mutual enjoyment.
      You may find different definitions, like pursuing the same goal, helping each other or following rules and requests.
      Feel free to believe in the one that resonate with you most. We compared many of them and the one we understood was worth of spreading was the one based on mutual enjoyment. Here's why.

      First of all, in a cooperation everybody wins. It's like preparing a dinner together with friends, everybody eats. Moreover, it's not about winning, but about feeling good and establishing good relations: mutual enjoyment. So when you play competitive sports or when you're competitive in a market and someone loses, you're not neutral. Even when you are driven by a noble altruism and help someone it's not a cooperation: it is altruism, someone gets more than you and you are okay with it. Alliances against a third party aren't a cooperation: people in mafia help each other, at the expenses of someone else. Cooperation is therefore a balance of egoism and altruism, in which everybody enjoys the interaction. Even better, cooperation is being aware of complexity.

      If you're linear, you go straight to what you want, or, in the case of deep altruism, you dedicate yourself completely to someone else.
      A very selfish man may end up with no advantages once his reputation is gone. A profound altruist person may lack self respect and be even poorly effective in helping.
      When your intentions embrace complexity instead, you start considering many perspectives, including yourself and the environment. You coordinate those diverse perspectives and your actions gain awareness.

      Feel free to proceed in the learning area of the website. If you desire deeper understanding, join Cooperacy and participate to the online webinars, free for every member.
