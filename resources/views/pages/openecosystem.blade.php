    <p class="subheading freedom center">III - THE FINAL CIRCLE</p>
    <p class="base">New awareness is spread through the participants. The ideas proposed are listed and voted according to: costs and benefits; personal trust; emotional power; transparency; participants commitment; understandability; innovation and creative power of the idea, following the seven conditions of <a class="aue" href="/cooperation">cooperation</a>. From the voting review a future agenda is made based on a common dictionary, a redistribution of workload and resources and a common roadmap.</p><br>
