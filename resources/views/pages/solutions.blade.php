  <p class="subheading diversity center space margin">THE EVOLUTION OF CONTRAST</p>
  <p class="base"> Contrast between ideas or people may be evolved in different ways using creative proportionality. Following the conditions of cooperation, you may end up developing amazing solutions able to coordinate the diversities of the individuals in a group, avoiding conflict. Contrast is part of the game, conflict is instead a possible form of violence. Equivalence, Trust, Care, Transparency, Freedom, Understanding and Diversity are your keys to foster  solutions for evolving contrast in development and cooperation.</p>

  <p class="subheading equivalence center space margin">EQUIVALENCE: TURNS AND EXPERIMENTATION</p>
  <p class="base">Allow experimentation and use turns so that different proposed solutions have the same importance. Enjoy the way others would do things, as you learn how they think, and they learn when to follow you if they make a mistake or if their way is not the most enjoyable.</p>

  <p class="subheading trust center space margin">TRUST: TIME AND EVALUATION</p>
  <p class="base">Give time its pace, allow experience to fix the situation and evaluate the results after a given amount of time. Sometimes fear may make you think things will go bad. Faith in your solution and in that of the other allows things to set up, obviously when you do not have information that clearly shows the solution found has evidently damaged everyone for a relevant amount of time.</p>

  <p class="subheading care center space margin">CARE: BETTER CONTEXTS, BETTER FEELINGS</p>
  <p class="base">Check everything about the context, the environment and the services. Are you in a beautiful place? Are there plants or animals in good shape around you? Are there colors? Is there good food and water? Did you drink too much alcohol or smoke too much? Are you feeling good? Are you tired and need a pause? Are you afraid of something? Is there anything to talk about that can make you feel happy? If you are feeling bad, where in your body is that mood? Can you describe it and what could make you feel better? Can you talk for a while about a dream situation and how you would like the present situation to be? <br> These questions seem to be a lot but they are very important. They can create a funny and bright mood and give you time to relax a bit and be more comprehensive with each other.</p>

  <p class="subheading transparency center space margin">TRANSPARENCY: THE MISSING SECRETS</p>
  <p class="base">Find the missing secret, exposing everyone’s point of view in complete freedom. Be careful with this methodology and be ready to accept apparent unpleasant truths. In search for a proportional creative resolution, tell frankly what is the minimum you need to be comfortable in the current situation (this is quite similar to the "indaba" south african methodology). Express your desires, not only your needs, trying to be specific as much as you can and talking about yourself "like as if". A sentence like "You don't listen to me" becomes "I feel like as if you're not listening to me, could you give me 15 minutes a day, no more, in which you listen without interrupting?"</p>

  <p class="subheading freedom center space margin">FREEDOM: THE RIGHT SPACE TO EVERYONE</p>
  <p class="base">Giving the right space to people without separating is a very effective solution, as there’s nothing worse than forcing to be together people who do not want to. Try an Open Space Technology if the people are many, try a pause or simply some space or distance for a while if you are a few. Sometimes longer separation is needed.</p>

  <p class="subheading understanding center space margin">UNDERSTANDING: COMMON CODES</p>
  <p class="base">Ask yourself what are the values you believe in when you have a contrast. The values or the motivations could be profoundly similar. Choose together common objective and measurable criterias to evaluate the situation. Everyone of us is different, and everyone of us sees things with a different perspective, trying to survive in this complex world. The key in creating the feeling of cooperation is understanding that everyone sees the world with complete different codes, so only when you have common ways of interpreting the reality around you it's possible to comprehend each other.</p>

  <p class="subheading diversity center space margin">DIVERSITY: INTEGRATION AND ROLES</p>
  <p class="base">Sometimes contrast are based on two complementary visions about the problem which do not exclude that much one another, but can be integrated. Combining the two perspective in a creative way may result in a strong, effective and powerful solution.<br>If the contrast is instead based on individuals with different expertises, every one of them should have a role, based on their personality and knowledge so that everyone has a responsibility in which they believe and its proper space and responsibilities. Setting up some space between different skills is the way to integrate people instead of ideas. Everyone will feel part of the group without overlapping on other people.</p>

