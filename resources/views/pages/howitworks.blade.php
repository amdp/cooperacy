              <p class="subheading understanding center space margin">THE COOPERACY PROJECTS AS MODULES</p>
              <p class="base">As you see, there is a modular design in the projects. The modularity allows for an organic and circle-like approach that is able to create open source blueprints for the whole platform community. Any module (ideas, projects, ecosystems) can have a "home" and internal "parts".<br>
              Every module has:<br><br>
              1. Information set</p>
              <p class="base">
                • Title (and proposers when anonymity ends) <br>
                • Representative image <br>
                • Subheading or objective of the project<br>
                • Home and parts<br>
                • Tags<br>
                • Location<br>
                • Voting allowance (for specific local projects)<br></p>
              <p class="base">
              2. Coordinators: the ears of the module, they listen to what the participants want to do and execute it.<br>
              3. Participants: active hands of the module. Minimum 2, the coordinators, advised maximum 150.<br>
              4. Experts: possible special participants who do not vote but give advices. The “mouth” of the module.<br>
              5. Reporters: two “eyes” of the module, they write reports and compare the module with the business plan.<br>
              6. Hudget: the number of committed people required to realize the module.<br>
              7. Feed: the module is continuously subject of feedback by the participants and the voters. Even after years any participant can decide if something was good or bad, influencing the memory of the system. A simple feedback history is available: after 1 month (M), 1 season (S), 1 year (Y) and after 7 years (7).<br>
              Feedback can be only of three kinds: HAPPY(+) MEDIUM(=) SAD(-)<br><br>
              The projects represent the voting unit of Cooperacy. They have this lifecycle:<br> </p>
              <p class="base">
              •	Idea: the idea or module is proposed – it is anonymous;<br>
              •	Vote: the idea or module is voted – anonymously;<br>
              •	Birth: the idea becomes a project, anonymity ends, budget phase starts;<br>
              •	Life: the project becomes an ecosystem, its profits go back to the collection fund;<br>
              •	Death: when the mission is accomplished or terminated the project “dies”. This is not a bitter end, it means the project goes in the archive memory in order to influence future modules and for future reference;<br></p>
