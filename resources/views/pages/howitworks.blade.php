              <p class="base">Current minimum budget for funded ideas is 1000 euros, as according to crowdfunding sites statistics ideas with lower amounts averagely fail.<br>
              Any direct donation to an idea counts as dedicated donated budget. Donated budget and pre-allocated budget count for the idea budget to be turned into a project, but donated budget cannot be returned to the collection fund unless the idea is rescinded or abandoned.<br>
              Once the idea has reached the budget and/or hudget, the participants of the idea are kept frozen during the project phase until the project is turned into an ecosystem. The budget release milestones deadlines are chosen according to the Cooperacy <a class="auf" href="/governance">governance</a> scheme.<br>
              Eventual contrasts not resolved in the 7 budget milestones are solved with a transparent voting kept live for three days, in which the participants can change their votes. The coordinators have three more days to try to creatively integrate the contrasting positions according to the seven solutions. At the end of a week time, the milestone should be decided.<br>
              All the contrasting votes not integrated reduce the budget, which goes back to the collection fund. In that case, the project turns back to the idea state, keeping the milestones approved, and waits again for the fulfilment of the missing budget cut due to contrasting positions.<br>
              Highlighting colors can indicate different ideas types, like cyan for “educational”, violet for “scientific”, and green for “environmental” ones. You are free to use these labels, but if they are misleading they can make people downvote them. Some ecosystems are geographic units, like cities or specific locations and are highlighted in blue.<br>
              When an Idea becomes a project, the idea is “born”, and has a birthdate. When the project becomes an ecosystem it can receive a three level feedback by the coomembers (+,=,-).<br>
              In case of “death” of an ecosystem, for example when a course ends, or a backery is sold, its final step is to be archived. This turns the ecosystem into a “memory" and the AI analyzes it while the participants may still give some feedback.<br>
              Participants Gender (M / F / Other) will be aggregated in order to inidicate in the future the modules (ideas, projects, ecosystems) gender composition.<br>
              Ecosystems may have internal ideas, for example a language institute that wants to add a new language course. Budget ideas that want to be funded by the Cooperacy collection fund must be in the Cooperacy Home ecosystem to receive any budget pre-allocation. Only afterwards they can be inserted as part of any ecosystem.<br>
            </p>

            <p class="subheading understanding center space margin">CO-DONATIONS</p>
            <p class="base">Co-donation is an alternative way in which you can help an idea to find its budget. When you hit on co-donate, you choose a maximum amount you are willing to donate for filling up the idea budget, if all the other co-donators are willing to donate the same. The system periodically evaluates the lowest of all amounts promised and multiplies for the number of the co-donators in order to see if the total budget is reached. If not, the system evaluates other combination possibilities able to reach the budget, like excluding lower donation amounts. Once a possible co-donation is found, the idea is turned into a project and the codonations are billed to your bank account, credit card or any other possibility the website allows.</p>

            <p class="subheading understanding center space margin">BUDGET RELEASE</p>
            <p class="base">The budget release process is divided into 7 phases and every phase has a three days approval voting time in which the process could be halted and the people in charge can be changed for corruption or security reasons. Only people who upvoted, participated or are relative to the module will be notified of the phases. Participants are therefore allowed to vote even if they are external to the module but only if they really care, as they won’t be notified otherwise. Every step is also voted for its transparency and sustainability.</p><br>

